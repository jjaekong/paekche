												</li>
												<li>
													학생회의 활동을 지도육성하기 위하여 학생지도복지위원회를 두며, 학생지도복지위원회의 위원장은 총장이 되며, 부위원장은 종합민원센터장이 되고, 위원은 5인 이내로 구성하여 운영하며, 학생지도에 관한 규정은 총장이 따로 정한다.
